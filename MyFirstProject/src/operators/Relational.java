package operators;

public class Relational {
    public static void main(String[] args) 
    {
        int a = 10, b = 1;
        System.out.println("a is " + a + " and b is " + b);

        // == operator
        System.out.println(a == b);  // false

        // != operator
        System.out.println(a != b);  // true

        // > operator
        System.out.println(a > b);  // true

        // < operator
        System.out.println(a < b);  // false

        // >= operator
        System.out.println(a >= b);  // true

        // <= operator
        System.out.println(a <= b);  // false
    }
    
}
